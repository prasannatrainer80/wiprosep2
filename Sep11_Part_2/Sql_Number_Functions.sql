-- Number Functions

-- Abs() : Returns absolute value 

select abs(-12);

-- pow(n,m) : Returns n power m value 

select pow(2,3);

-- sqrt(n) : Returns sqrt value 

select sqrt(49);

-- Ceiling() : returns greatest integer value

select ceiling(12.00000001);

-- floor() : returns smallest integer value 

select floor(12.999999999);

-- Round() : used to round the value to particular digits

select round(8788.8766,3);